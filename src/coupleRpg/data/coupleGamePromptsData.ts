function proRows(
  _mode: string,
  items: Array<[string, string, string, RawPromptRow['tone']?]>
): RawPromptRow[] {
  return items.map(([title, category, emoji, tone]) => ({
    title,
    category,
    emoji,
    tone: tone ?? 'sweet',
  }));
}

